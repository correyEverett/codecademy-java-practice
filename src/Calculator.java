public class Calculator {

    // constructor
    public Calculator() {

    }

    // add method
    public int add(int a, int b) {
        int sum = a + b;
        return sum;
    }

    // subtract method
    public int subtract(int a, int b) {
        int difference = a - b;
        return difference;
    }

    // multiply method
    public int multiply(int a, int b) {
        int product = a * b;
        return product;
    }

    // divide method
    public int divide(int a, int b) {
        int quotient = a / b;
        return quotient;
    }

    // modulo method
    public int modulo(int a, int b) {
        int remainder = a % b;
        return remainder;
    }





}
