public class Calculator {

    // constructor
    public Calculator() {

    }

    // add method
    public int add(int a, int b) {
        int sum = a + b;
        return sum;
    }

    // subtract method
    public int subtract(int a, int b) {
        int difference = a - b;
        return difference;
    }





}
